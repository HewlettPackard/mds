package com.hpl.erk.config.type;

import com.hpl.erk.ReadableString;
import com.hpl.erk.config.ex.ReadError;
import com.hpl.erk.types.TypeToken;

public class ClassType extends SimpleType<Class<?>> {
  public ClassType() {
    super(TypeToken.find(Class.class));
  }
  @Override
  public Class<?> readVal(ReadableString input, String valTerminators) throws ReadError {
    int resetTo = input.getCursor();
    String name = readUnquoted(input, valTerminators);
    Class<?> c;
    try {
      c = Class.forName(name);
    } catch (ClassNotFoundException e) {
      throw new ReadError(input, resetTo, this, "Unknown class: %s", name);
    }
    return c;
  }


}
