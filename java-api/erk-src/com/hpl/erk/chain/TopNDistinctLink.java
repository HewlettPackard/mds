package com.hpl.erk.chain;

import java.util.Comparator;
import java.util.Iterator;

import com.hpl.erk.TopN;
import com.hpl.erk.func.SourceExhausted;

public class TopNDistinctLink<Head, In> extends EagerLink<Head, In, In> {
  protected final int n;
  protected final Comparator<? super In> comparator;
  protected TopN<In> topN;
  int next;
  
  protected TopNDistinctLink(Chain<Head, ? extends In> pred, int n, Comparator<? super In> comparator) {
    super(pred);
    this.n = n;
    this.comparator = comparator;
  }

//  protected TopNLink(Chain<Head, ? extends In> pred, int n) {
//    this(pred, n);
//  }
  
  @Override
  public void activate() {
    if (complete) {
      topN = new TopN<>(n, comparator);
    }
    next = 0;
    super.activate();
  }

  @Override
  protected boolean see(In elt) {
    topN.add(elt);
    return true;
  }
  
  @Override
  public int expectedSize() {
    int pSize = pred.expectedSize();
    if (pSize == Integer.MAX_VALUE || pSize < n) {
      return pSize;
    }
    return n;
  }

  @Override
  public Context createContext() {
    return new Context() {
      Iterator<In> iter = topN.iterator();
      @Override
      public In produce() throws SourceExhausted {
        exhaustedIf(!iter.hasNext());
        return iter.next();
      }};
  }

  @Override
  public Flow pipeInto(final Receiver<? super In> sink) {
    return new Flow() {
      @Override
      public void perform() {
        for (In elt : topN) {
          if (sink.receive(elt)) {
            return;
          }
        }
        sink.close();
      }
    };
  }
  

}
