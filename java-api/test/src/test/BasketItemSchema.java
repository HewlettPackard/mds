package test;

import java.util.*;
import com.hpl.mds.annotations.*;
import com.hpl.mds.*;
import static com.hpl.mds.MDS.*;

@RecordSchema
@TypeName(name="mds-test-%1$s")
public interface BasketItemSchema {
  @Final Product product();
  int quantity();

  static String toString(BasketItem.Private self) {
    return String.format("%,d of %s",
                         self.getQuantity(),
                         self.getProduct());
  }
	
  static void BasketItem(BasketItem.Constructing self,
                         Product product,
                         int quantity)
  {
    self.setProduct(product);
    self.setQuantity(quantity);
  }

  static void purchase(BasketItem.Private self) {
    Product p = self.getProduct();
    int q = self.getQuantity();
    System.out.format("Purchasing %d of %s%n", q, p);
    p.removeStock(q);
    System.out.format("Now %s%n", p);
  }
	
}
