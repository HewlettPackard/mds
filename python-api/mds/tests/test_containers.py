class TestTask(unittest.TestCase):

    def setUp(self):
        self.isoctxt = IsolationContext.get_for_process().create_child()
        self.assertTrue(self.isoctxt.is_publishable)
        self.task = Task()

    def test_get_parent(self):
        parent = self.task.parent

        self.assertIsNotNone(parent)
        self.assertIsInstance(parent, Task)
        self.assertNotEqual(self.task, parent)
        self.assertNotEqual(id(self.task), id(parent))

    def __run_callable(self, fn=None, args=tuple()):
        self.isoctxt.call(fn, args)

    def test_task_lambda(self):
        # TODO: Need to test these with different Python types
        def w1():
            Task(lambda: 2 * 100).run()

        def w2(x, y):
            Task(lambda x, y: x * y, (x, y)).run()

        self.__run_callable(fn=w1, args=tuple())
        self.__run_callable(fn=w2, args=(2, 100))

    def test_task_function(self):
        def w1():
            def fn1():
                x = 1
                y = 2
                z = x * y

                return z

            Task(fn1).run()

        def w2(x, y):
            def fn2(x, y):
                z = x * y

                return z

            Task(fn2, (x, y)).run()

        self.__run_callable(fn=w1)
        self.__run_callable(fn=w2, args=(2, 100))
    
    def test_task_functor(self):
        def w1(*args):
            class Functor(object):

                def __call__(self, x=42, y=424):
                    return x * y

            Task(Functor(), args=args).run()

        self.__run_callable(fn=w1)
        self.__run_callable(fn=w1, args=(2424, 122))

    @unittest.skip("Debug - this kills the heap")
    def test_task_mdsarray(self):
        l = BoolArray.create(1000)

        self.assertFalse(l[0])
        self.assertFalse(l[1])
        self.assertFalse(l[2])

        def w1(ref):
            def worker(ref):
                ref[1] = True

            t = Task(worker, (ref,))
            t.run()

        self.__run_callable(fn=w1, args=(l,))
 
        self.assertFalse(l[0])
        self.assertTrue(l[1])
        self.assertFalse(l[2])


class TestIsolationContexts(unittest.TestCase):

    KINDS = ["live", "read_only", "detached"]

    def setUp(self):
        self.base = IsolationContext.get_global()
        self.seen_hashes = set()

    def test_get_global(self):
        self.assertTrue(hasattr(IsolationContext, "get_global"))
        ctxt = IsolationContext.get_global()
        self.assertFalse(ctxt is None)

    def _generate_tree(self, level=1, max_level=5):
        for kind in self.KINDS:
            for snapshot in (True, False):
                if level == max_level:
                    yield IcConfig(kind, snapshot, None)
                else:
                    children = self._generate_tree(level + 1, max_level)
                    yield IcConfig(kind, snapshot, children)

    def _test_tree(self, tree, parent, level=0, verbose=False):
        for config in tree:
            if verbose:
                print("{}{} {} {}".format(
                    "  " * level,
                    "`-" if level > 0 else "",
                    config.kind,
                    "[SNAPSHOT]" if config.snapshot else ""
                ))
            try:
                ctxt = parent.create_child(config.kind, config.snapshot)
                hash_val = hash(ctxt)

                self.assertTrue(hash_val not in self.seen_hashes)
                self.seen_hashes.add(hash_val)

                if config.kind == "read_only":
                    self.assertTrue(ctxt.is_read_only)
                    self.assertFalse(ctxt.is_publishable)
                elif config.kind == "detached":
                    self.assertFalse(ctxt.is_read_only)
                    # TODO: Should this be publishable? Things publish to it...
                else:  # live
                    pass # TODO: What to test?

                # TODO: self.assertTrue(config.snapshot and ctxt.is_snapshot)

                if config.children is not None:
                    self._test_tree(config.children, ctxt, level + 1, verbose)
            except Exception as e:
                if verbose:
                    print("{}`=> Exception: {}".format("  " * (level + 1), e))

    def test_create_children(self):
        self._test_tree(self._generate_tree(max_level=5), self.base)

